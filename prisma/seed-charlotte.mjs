async function main() {
  console.log("Fetching questions...");
  const questions = await prisma.question.findMany();
  if (questions.length === 0) {
    console.error("No questions found — run the main seed first (node prisma/seed.mjs)");
    process.exit(1);
  }
  const qByKey = Object.fromEntries(questions.map((q) => [q.key, q.id]));

  for (const userData of TEST_USERS) {
    // Upsert user
    const user = await prisma.user.upsert({
      where: { email: userData.email },
      update: { name: userData.name, onboardingStep: "COMPLETE" },
      create: { email: userData.email, name: userData.name, onboardingStep: "COMPLETE" },
    });

    // Upsert profile
    const { latitude, longitude, ...profileRest } = userData.profile;
    await prisma.profile.upsert({
      where: { userId: user.id },
      update: { ...profileRest, latitude, longitude },
      create: { userId: user.id, ...profileRest, latitude, longitude },
    });

    // Upsert answers
    for (const [key, value] of Object.entries(userData.answers)) {
      const questionId = qByKey[key];
      if (!questionId) continue;
      await prisma.questionnaireAnswer.upsert({
        where: { userId_questionId: { userId: user.id, questionId } },
        update: { value: JSON.stringify(value) },
        create: { userId: user.id, questionId, value: JSON.stringify(value) },
      });
    }

    // Upsert priorities — default everything to SOMEWHAT_IMPORTANT, override specified ones
    for (const q of questions) {
      const importance = userData.priorities[q.key] ?? "SOMEWHAT_IMPORTANT";
      await prisma.answerPriority.upsert({
        where: { userId_questionId: { userId: user.id, questionId: q.id } },
        update: { importance },
        create: { userId: user.id, questionId: q.id, importance },
      });
    }

    console.log(`  ✓ ${userData.name} (${userData.profile.location})`);
  }

  console.log(`\nSeeded ${TEST_USERS.length} Charlotte test profiles.`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
